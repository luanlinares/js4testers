//OPERADORES - Matemáticos, de Comparação, Lógicos

//Operadores Aritiméticos
console.log(2+2);
console.log(2-2);
console.log(2*2);
console.log(2/2);

//Operadores de Comparação
console.log(2==3); //Igual
console.log(2!=1); //Não igual ou diferente
console.log(2==="2"); //Igual estrito(compara valor e tipo da variável)
console.log(2!=="2"); //Não igual estrito
console.log(2>1); //Maior
console.log(2<1); //Menor
console.log(2>=1); //Maior ou igual
console.log(2<=1); //Menor ou igual